const router = Router();

function param(req: Request, name: string): string {
  const v = req.params[name];
  if (!v) throw new AppError(400, 'MISSING_PARAM', `Missing route parameter: ${name}`);
  return Array.isArray(v) ? v[0] ?? '' : v;
}

